document.getElementById('main_content_navigation_button1').addEventListener('click', function() {
    let floatingBlock = document.querySelector('#initiation');
    if (floatingBlock.style.display === 'none' || floatingBlock.style.display === '') {
        floatingBlock.style.display = 'flex';
    } else {
        floatingBlock.style.display = 'none';
    }
});

